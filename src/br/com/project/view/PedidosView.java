    private void salvarBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarBTActionPerformed

        try {
            ped.setDataPedido(dataPedidoDC.getDate());
            ped.setPedidoprodutosList(listpp);
            ped.setObs(obsField.getText());
            ped.setAprovacao(aprovadoCheck.isSelected());
        } catch (NullPointerException e) {
            ped = new Pedidos();
            ped.setDataPedido(dataPedidoDC.getDate());
            ped.setPedidoprodutosList(listpp);
            ped.setObs(obsField.getText());
            ped.setAprovacao(aprovadoCheck.isSelected());
        }
        new PedidosBean().salvar(ped);

        this.limparBTActionPerformed(evt);
    }//GEN-LAST:event_salvarBTActionPerformed

