    private void organizaTabela() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Nome");
        model.addColumn("Quantidade");
        model.addColumn("Medida");
        try {
            for (int i = 0; i < listpp.size(); i++) {
                model.addRow(new String[]{
                    listn.get(i),
                    String.valueOf(listpp.get(i).getQuantidade()),
                    listpp.get(i).getProdutos().getMedida(),});
            }
        } catch (NullPointerException e) {
            
        }
        jTable1.setModel(model);
    }

    private void organizaTabela1() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Nome");
        model.addColumn("Quantidade");
        model.addColumn("Medida");

        for (int i = 0; i < listpp.size(); i++) {
            model.addRow(new String[]{
                listpp.get(i).getProdutos().getNome(),
                String.valueOf(listpp.get(i).getQuantidade()),
                listpp.get(i).getProdutos().getMedida(),});
        }
        jTable1.setModel(model);
    }

    public void recebeDetalhes(Pedidos ped) {
        this.ped = ped;
        dataPedidoDC.setDate(ped.getDataPedido());
        aprovadoCheck.setSelected(ped.getAprovacao());
        listpp.addAll(ped.getPedidoprodutosList());
        jButton1.setEnabled(false);
        jButton2.setEnabled(false);
        dataPedidoDC.setEnabled(false);
        aprovadoCheck.setEnabled(true);

        this.organizaTabela1();
        if (ped.getAprovacao()) {
            jButton3.setVisible(true);
            jSeparator4.setVisible(true);
        } else {
            jButton3.setVisible(false);
            jSeparator4.setVisible(false);
        }
    }

    public void setAcessibilidade() {
        JRootPane meurootpane = getRootPane();
        meurootpane.getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW).put(KeyStroke.getKeyStroke(KeyEvent.VK_ESCAPE, 0), "ESCAPE");
        meurootpane.getRootPane().getActionMap().put("ESCAPE", new AbstractAction("ESCAPE") {
            @Override
            public void actionPerformed(ActionEvent e) {
                int i = JOptionPane.showConfirmDialog(null, "Deseja realmente sair?");
                if (i == 0) {
                    dispose();
                }

            }
        });
    }
