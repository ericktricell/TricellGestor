    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (loginField.getText().equals("ADMIN") && senhaField.getText().equals("gestor")) {
            this.setUser("Super");
            this.dispose();
        } else {
            String s = EncriptaSenha.encripta(senhaField.getText());
            if (new UsuarioBean().validaAcesso(loginField.getText(), s) != null){
                this.setUse(new UsuarioBean().validaAcesso(loginField.getText(), s));
                this.setUser(this.use.getTipo());
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Usuario ou senha inv√°lidos");
                loginField.setText("");
                senhaField.setText("");
                loginField.requestFocus();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

