    private void salvarBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarBTActionPerformed
        if (salvarBT.getText().equals("DETALHES")){
            PedidosView p = new PedidosView(new javax.swing.JFrame(), true);
            p.recebeDetalhes(list.get(row));
            p.setVisible(true);
            this.carregaDados();
            this.organizaTabela();
            salvarBT.setEnabled(false);
        } else {
            try {
                new relatorioProgress(new javax.swing.JFrame(), true).setVisible(true);
                new PedidodeCompras().emite(list.get(row).getIdPedido(), list.get(row));
            } catch (DocumentException ex) {
                Logger.getLogger(VerPedidosView.class.getName()).log(Level.SEVERE, null, ex);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(VerPedidosView.class.getName()).log(Level.SEVERE, null, ex);
            }
            salvarBT.setEnabled(false);
        }
    }//GEN-LAST:event_salvarBTActionPerformed

