    private void carregaDados(){
        list = new ArrayList<>();
        list = new PedidosBean().listPedidos();
        this.organizaTabela();
    }
    
    private void organizaTabela(){
        SimpleDateFormat mask = new SimpleDateFormat("dd/MM/yyyy");
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Data Pedido");
        model.addColumn("Aprovação");
        
        
        for(Pedidos ped : list){
        String apr = "Negado";
            if (ped.getAprovacao())
                apr = "Aprovado";
            
            model.addRow(new String[]{
                mask.format(ped.getDataPedido()),
                apr
        });
        }
        
        jTable1.setModel(model);
    }
    
    public void verUser(String tipoUsuario){
        if(tipoUsuario.equals("Usuario")){
            salvarBT.setText("Emitir");
            salvarBT.setIcon(new javax.swing.ImageIcon(getClass().getResource("/br/com/eagestor/icones/relatorio1.png")));
            //list = new PedidosBean().listPedidosUser();
            this.organizaTabela();
        }
    }
    
    public void setAcessibilidade() {
        JRootPane meurootpane = getRootPane();
        meurootpane.getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW).put(KeyStroke.getKeyStroke(KeyEvent.VK_ESCAPE, 0), "ESCAPE");
        meurootpane.getRootPane().getActionMap().put("ESCAPE", new AbstractAction("ESCAPE") {
            @Override
            public void actionPerformed(ActionEvent e) {
                int i = JOptionPane.showConfirmDialog(null, "Deseja realmente sair?");
                if (i == 0){
                    dispose();
                }
                
            }
        });
    }
