    private void carregaCombo(){
        list = new FornecedorBean().listFornecedor();
        
        fornecedorCB.removeAllItems();
        for (Fornecedor f : list){
            fornecedorCB.addItem(f.getNomeFantasia());
        }
    }
    
    private void organizaTabela(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Produto");
        model.addColumn("Quantidade");
        model.addColumn("Medida");
        model.addColumn("Valor Compra");
        try{
        for (Compraprodutos cp : listcp){
            model.addRow(new String[]{
                cp.getProdutos().getNome(),
                String.valueOf(cp.getQuantidade()),
                cp.getProdutos().getMedida(),
                new DecimalFormat("¤#,##0.00").format(cp.getValorCompra())
            });
        }
        }catch(NullPointerException e){}
        jTable1.setModel(model);
    }
    
    public void setAcessibilidade() {
        JRootPane meurootpane = getRootPane();
        meurootpane.getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW).put(KeyStroke.getKeyStroke(KeyEvent.VK_ESCAPE, 0), "ESCAPE");
        meurootpane.getRootPane().getActionMap().put("ESCAPE", new AbstractAction("ESCAPE") {
            @Override
            public void actionPerformed(ActionEvent e) {
                int i = JOptionPane.showConfirmDialog(null, "Deseja realmente sair?");
                if (i == 0){
                    dispose();
                }
                
            }
        });
    }
