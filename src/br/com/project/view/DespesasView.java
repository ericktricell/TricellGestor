    private void salvarBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarBTActionPerformed
        try {
            dep.setDescricao(descricaoField.getText());
            dep.setPago(pagoCB.isSelected());
            dep.setVencimento(vencimentoField.getDate());
            dep.setValor(Double.parseDouble(valorField.getText().substring(2).replace(",", ".")));
            dep.setDataPagamento(dataPag.getDate());
        } catch (NullPointerException e) {
            dep = new Despesas();
            dep.setDescricao(descricaoField.getText());
            dep.setPago(pagoCB.isSelected());
            dep.setVencimento(vencimentoField.getDate());
            dep.setValor(Double.parseDouble(valorField.getText().substring(2).replace(",", ".")));
            dep.setDataPagamento(dataPag.getDate());
        }
        new DespesasBean().salvar(dep);
        this.jButton1ActionPerformed(evt);
    }//GEN-LAST:event_salvarBTActionPerformed

