    private void carregaCategoria() {
        categoriaCB.removeAllItems();
        listCat = new CategoriaBean().listaCategorias();
        categoriaCB.addItem("Selecione");
        for (Categoria cat : listCat) {
            categoriaCB.addItem(cat.getCategoria());
        }
    }

    private void organizaTabela() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Nome");
        model.addColumn("Marca");
        model.addColumn("Quantidade");
        model.addColumn("Medida");
        model.addColumn("Valor Compra");
        model.addColumn("Valor Venda");

        DecimalFormat df = new DecimalFormat("¤#,##0.00");

        for (int i = 0; i < listProd.size(); i++) {
            model.addRow(new String[]{
                listProd.get(i).getNome(),
                listProd.get(i).getMarca(),
                listProd.get(i).getQuantidade().toString(),
                listProd.get(i).getMedida(),
                df.format(listProd.get(i).getValorCompra()),
                df.format(listProd.get(i).getValorVenda()),});
        }
        jTable1.setModel(model);
    }

    private void carregaProdutos() {
        listProd = new ProdutosBean().listProdutos();
        this.organizaTabela();
    }

    private void carregaMedidas() {
        listMed = new MedidasServ().getLsMedidas();
        medidaCB.removeAllItems();
        medidaCB.addItem("Selecione");
        for (Medidas med : listMed) {
            medidaCB.addItem(med.getDescricao() + " - " + med.getValor());
        }
    }

    private void calculaProdutos() {
        double qnt = 0;
        for (Produtos p : listProd) {
            qnt += p.getQuantidade();
        }

        totalProdutos.setText(String.valueOf(qnt));
    }

