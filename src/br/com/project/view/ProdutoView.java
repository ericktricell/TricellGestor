    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();
        prod = listProd.get(row);

        nomeField.setText(prod.getNome());
        descricaoField.setText(prod.getDescricao());
        marcaField.setText(prod.getMarca());
        codigoField.setText(prod.getCodigo());
        codNcmField.setText(prod.getCodNcm());

        for (int i = 0; i < listCat.size(); i++) {
            if (Objects.equals(prod.getIdCategoria().getIdCategoria(), listCat.get(i).getIdCategoria())) {
                categoriaCB.setSelectedIndex(i + 1);
                break;
            }
        }
        for (int i = 0; i <= listMed.size(); i++) {
            if (prod.getMedida().equals(listMed.get(i).getValor())) {
                medidaCB.setSelectedIndex(i);
                break;
            }
        }
        flag = 1;
    }//GEN-LAST:event_jTable1MouseClicked

