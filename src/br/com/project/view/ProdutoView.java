    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jComboBox1.getSelectedIndex() == 0) {
            double vlrCompra = 0;
            for (Produtos p : listProd) {
                vlrCompra += p.getValorCompra() * p.getQuantidade();
            }
            capitalTotal.setText(new DecimalFormat("¤#,##0.00").format(vlrCompra));
        } else {
            double vlrVenda = 0;
            for (Produtos p : listProd) {
                vlrVenda += p.getValorVenda() * p.getQuantidade();
            }
            capitalTotal.setText(new DecimalFormat("¤#,##0.00").format(vlrVenda));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

