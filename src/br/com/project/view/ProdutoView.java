    private void salvarBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarBTActionPerformed

        if (categoriaCB.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Selecione uma categoria");
        } else if (medidaCB.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Selecione uma medida");
        } else {
            try {
                if(prod.getQuantidade() == null){
                    prod.setQuantidade(0.0);
                }
                prod.setCodigo(codigoField.getText());
                prod.setDescricao(descricaoField.getText());
                prod.setIdCategoria(listCat.get(categoriaCB.getSelectedIndex() - 1));
                prod.setInativo(inativoCHB.isSelected());
                prod.setMarca(marcaField.getText());
                prod.setMedida(listMed.get(medidaCB.getSelectedIndex() - 1).getValor());
                prod.setNome(nomeField.getText());
                prod.setCodNcm(codNcm.getCodigo());
            } catch (NullPointerException e) {
                prod = new Produtos();
                if(prod.getQuantidade() == null){
                    prod.setQuantidade(0.0);
                }
                prod.setCodigo(codigoField.getText());
                prod.setDescricao(descricaoField.getText());
                prod.setIdCategoria(listCat.get(categoriaCB.getSelectedIndex() - 1));
                prod.setInativo(inativoCHB.isSelected());
                prod.setMarca(marcaField.getText());
                prod.setMedida(listMed.get(medidaCB.getSelectedIndex() - 1).getValor());
                prod.setNome(nomeField.getText());
                prod.setCodNcm(codNcm.getCodigo());
            }
            new ProdutosBean().salvar(prod);

            this.limparBTActionPerformed(evt);
            this.carregaProdutos();
        }
    }//GEN-LAST:event_salvarBTActionPerformed

